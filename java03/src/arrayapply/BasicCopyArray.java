package arrayapply;

public class BasicCopyArray {

	public static void main(String[] args) {
		int math1 = 100;
		int math2 = math1;
		System.out.println("수학1 : " + math1);
		System.out.println("수학2 : " + math2);
		math2 = 90;
		System.out.println("수학1 : " + math1);
		System.out.println("수학2 : " + math2);
	}

}
